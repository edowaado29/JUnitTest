package com.example.junittest

class Calculator {

    // fungsi untuk melakukan operasi penjumlahan dua bilangan
    fun add(a: Int, b: Int): Int {
        // mengembalikan nilai dari hasil penjumlahan a dan b
        return a + b
    }

    // fungsi untuk melakukan operasi pengurangan dua bilangan
    fun subtract(a: Int, b: Int): Int {
        // mengembalikan nilai dari hasil pengurangan a dan b
        return a - b
    }
}